
lis = [2, 1, 3, 5, 3, 8]

lis.sort()

print ("List elements after sorting are : ", end="")
for i in range(0, len(lis)):
	print(lis[i], end=" ")
	
print("\r")

lis.pop()


print ("List elements after pop are : ", end="")
for i in range(0, len(lis)):
	print(lis[i], end=" ")


lis.append([67,89,23])


print ("List elements after append are : ", end="")
for i in range(0, len(lis)):
	print(lis[i], end=" ",)


 

    


